# Định nghĩa hàm sum(a, b) để tính và in ra tổng
def sum(a, b):
    # Tính tổng
    total = a + b
    # In ra kết quả theo format mong muốn
    print(f"Tổng: {a} + {b} = {total}")

# --- Các lệnh gọi hàm ---

# 1. Tính tổng 4, 5
print("1. Tính tổng 4 và 5:")
sum(4, 5)

# 2. Tính tổng 3, 7
print("\n2. Tính tổng 3 và 7:")
sum(3, 7)
